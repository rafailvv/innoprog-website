function buildCourseKeywords(aliases: readonly string[], technologies: readonly string[] = []) {
  const intentPatterns = aliases.flatMap((alias) => [
    `курс ${alias}`,
    `курсы ${alias}`,
    `${alias} курсы`,
    `${alias} с нуля`,
    `${alias} для начинающих`,
    `обучение ${alias}`,
    `обучение ${alias} с нуля`,
    `онлайн обучение ${alias}`,
    `онлайн курс ${alias}`,
    `программа курса ${alias}`,
    `${alias} цена курса`,
    `${alias} с наставником`,
    `${alias} с трудоустройством`,
    `профессия ${alias}`,
  ]);
  const technologyPatterns = technologies.flatMap((technology) => [
    `курс ${technology}`,
    `обучение ${technology} с нуля`,
    `${technology} для начинающих`,
  ]);

  return unique([...intentPatterns, ...technologyPatterns]);
}

