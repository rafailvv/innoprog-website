const REVIEWS_DIRECTION_KEYS = new Set<ReviewsDirectionKey>(
  REVIEWS_DIRECTIONS.map((direction) => direction.key),
);

export function normalizeReviewsDirectionKey(value?: string | null): ReviewsDirectionKey {
  if (!value) {
    return ALL_REVIEWS_DIRECTION_KEY;
  }

  const normalized = decodeURIComponent(value).toLowerCase().trim();

  if (REVIEWS_DIRECTION_KEYS.has(normalized as ReviewsDirectionKey)) {
    return normalized as ReviewsDirectionKey;
  }

  if (normalized === "data" || normalized === "analytics" || normalized === "data-analytics") {
    return "data-science";
  }

  if (normalized === "c++" || normalized === "cpp-developer") {
    return "cpp";
  }

  if (normalized === "front" || normalized === "frontend-developer") {
    return "frontend";
  }

  if (normalized === "mobile-developer") {
    return "mobile";
  }

  return ALL_REVIEWS_DIRECTION_KEY;
}

export function getReviewsDirectionPath(direction: ReviewsDirectionKey) {
  return direction === ALL_REVIEWS_DIRECTION_KEY ? "/reviews" : `/reviews?direction=${direction}`;
}

export function getReviewCoursePath(direction: StudentReview["direction"]) {
  return REVIEW_COURSE_PATH_BY_DIRECTION[direction];
}

export function getStudentReviewSlug(review: StudentReview) {
  return review.courseReviewKey || review.id;
}

export function getStudentReviewPath(reviewOrId: StudentReview | string) {
  const review = typeof reviewOrId === "string" ? findStudentReviewByRouteSlug(reviewOrId) : reviewOrId;

  if (!review) {
    return "/reviews";
  }

  return `${getReviewsDirectionPath(review.direction)}&review=${encodeURIComponent(getStudentReviewSlug(review))}`;
}

